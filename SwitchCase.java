class SwitchCase
{
	public static void main(String[] args)
	{
		char choice = 's';
		switch(choice)
		{
			case 10: System.out.println(" case 10 ");
					break;
			case 15: System.out.println(" case 15 ");
					break;
			case 's': System.out.println(" case s ");
					break;
			case 'a': System.out.println(" case a ");
					break;
			case 'i': System.out.println(" case i ");
					break;			
			default:System.out.println(" case default ");
		}
	}
}


